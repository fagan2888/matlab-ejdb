    {{{1,2},{3,4}},{{5,6},{7,8}}} ...
  };
  for i = 1:numel(fixtures)
    value1 = fixtures{i};
    bson_value = bson.encode(value1);
    value2 = bson.decode(bson_value);
    disp(value1);
    disp(value2);
  end

end